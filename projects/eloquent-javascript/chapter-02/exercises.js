function fizzBuzz() {

  let number = 1; 
while(number < 16){
  
  if(number % 15 === 0){
    console.log('fizzbuzz');
    
  } else if(number % 5 === 0){
    console.log('buzz'); 
    
  }else if(number % 3 === 0){
    console.log('fizz'); 
    
  }else{
    console.log(number); 
  }
  number++; 
}

  
}

